    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed
        this.Numero2 = Float.parseFloat(this.pantalla.getText());
        
        switch(this.Operador){
            case "+": this.pantalla.setText(sinElPuntoCero(this.Numero1+this.Numero2)); break;
            case "-": this.pantalla.setText(sinElPuntoCero(this.Numero1-this.Numero2)); break;
            case "*": this.pantalla.setText(sinElPuntoCero(this.Numero1*this.Numero2)); break;
            case "/":if(this.Numero2==0){
                this.pantalla.setText("Error");
            }
               else{ 
                this.pantalla.setText(sinElPuntoCero(this.Numero1/this.Numero2)); break;}
        }
        
        
    }//GEN-LAST:event_jButton20ActionPerformed

